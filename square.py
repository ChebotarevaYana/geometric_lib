def area(a):  #определяем функцию area, принимающую аргумент a (сторона квадрата)
    return a * a  #возвращаем площадь квадрата, вычисленную по формуле
def perimeter(a):  #определяем функцию perimeter, принимающую аргумент a (сторона квадрата)
    return 4 * a  #возвращаем периметр квадрата, вычисленную по формуле