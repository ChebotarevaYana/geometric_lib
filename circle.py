import math #импортируем библиотеку math для использования математических функций и констант, таких как π (пи)
def area(r): #определяем функцию area, принимающую аргумент r (радиус окружности)
    return math.pi * r * r #возвращаем площадь круга, вычисленную по формуле.
def perimeter(r): #определяем функцию area, принимающую аргумент r (радиус окружности)
    return 2 * math.pi * r #возвращаем периметр круга, вычисленный по формуле