def area(a, b, c):  #определяем функцию area, принимающую аргументы a, b и c (стороны треугольника)
    return (a + b + c) / 2  #возвращаем площадь треугольника по формуле Герона
def perimeter(a, b, c):  #определяем функцию perimeter, принимающую аргументы a, b и c (стороны треугольника)
    return a + b + c  #возвращаем периметр треугольника, вычисленную по формуле
